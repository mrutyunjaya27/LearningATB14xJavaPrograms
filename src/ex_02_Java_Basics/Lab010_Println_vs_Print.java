package ex_02_Java_Basics;

public class Lab010_Println_vs_Print {

	public static void main(String[] args) {
		System.out.println("Hi");
		System.out.println("Bye"); //' ln- new line
		System.out.print("Hi");
		System.out.print("Bye");
		System.out.print("Hi "); //Space
		System.out.print("Bye");
	}

}
