package ex_03_Operators;

public class Lab025_Operators_Arithmetic_Operators {

	public static void main(String[] args) {
		
		//      +   (Addition)
		//      ﻿-   (Subtraction)
		//      ﻿*   (Multiplication)
		//      ﻿/   (Division)
		//      ﻿%   (Modulus)
		
		    int a = 20;
		    int b = 3;
		    float c = 3.0f;
		    
		    System.out.println(a+b);
		    System.out.println(a-b);
		    System.out.println(a*b);
		    System.out.println(a/b);
		    
		    System.out.println(a/c);
		    System.out.println("a+b"); // String so no arithmetic operator
		    System.out.println(a+b);

		

	}

}
