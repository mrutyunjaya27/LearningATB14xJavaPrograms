package ex_03_Operators;

public class Lab028_Relational_Operators {

	public static void main(String[] args) {
    //  < Less than
    //  <= Less than or equal to
    //  >  Greater than
	//  >= Greater or equal to
	//  == Equal to
	//  != Not equal to
		
		int a = 10;
		int b = 30;
		boolean c = a > b; // 10 > 30   //10 greater than 30
		System.out.println(c);
		
		int age_guddi = 28;
		int age_buddu = 30;
		
		boolean result = age_buddu >= age_guddi;   // who has the age greater or buddu age is greater or equal to guddi
		System.out.println(result);
		
	}

}
